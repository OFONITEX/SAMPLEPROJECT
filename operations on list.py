# accessing items in a list

list1= ["joy", "okon", "mary", "peter", "Glory"]
list1.insert(2, "bob")
print(list1)


list1= ["joy", "okon", "mary", "peter", "Glory"]
list1[2] = "Bill"
print(list1)

list1= ["joy", "okon", "mary", "peter", "Glory"]
del list1[2]
print(list1)

list1= ["joy", "okon", "mary", "peter", "Glory"]
list1.remove("joy")
print(list1)


list1= ["joy", "okon", "mary", "peter", "Glory"]

list1.pop(0)
print(list1)


